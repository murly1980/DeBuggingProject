    private void updateBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_updateBtnActionPerformed
        String selectedData=null;
        Question questions=new Question();
        questions.setQuestionId(Integer.parseInt(questionIDFld.getText()));
        questions.setQuestion(questionArea.getText());
        questions.setOption1(option1Fld.getText());
        questions.setOption2(option2Fld.getText());
        questions.setOption3(option3Fld.getText());
        questions.setOption4(option4Fld.getText());
        questions.setRightAnswer((String) rightOptionComboBox.getSelectedItem());
       
           
        SearchTableIF searchTable=new SearchTableIF();
       
       searchTable.UpdatedTable(questions);
        
          this.dispose();
           int[] selectedRow = searchTable.questionTable.getSelectedRows();
            int[] selectedColumns = searchTable.questionTable.getSelectedColumns();
            for (int k = 0; k < selectedRow.length; k++) {
                for (int j = 0; j < selectedColumns.length; j++) {
                    
                        selectedData = (String)searchTable. questionTable.getValueAt(selectedRow[k], selectedColumns[j]);
                        System.out.println("Selected: " + selectedData);
            QuestionService.updateQuestion(questions);
                }
            }
              JOptionPane.showMessageDialog(this, "Record Updated");
           
       
       
       
       
    }//GEN-LAST:event_updateBtnActionPerformed

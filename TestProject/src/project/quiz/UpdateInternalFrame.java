    public  void questionToBeUpdated(Question questn){
        // SearchTableIF table=new SearchTableIF();
           //table.getValue();
           questionIDFld.setText(String.valueOf(questn.getQuestionId()));
           questionIDFld.setEditable(false);
           questionArea.setText(questn.getQuestion());
           option1Fld.setText(questn.getOption1());
           option2Fld.setText(questn.getOption2());
          option3Fld.setText(questn.getOption3());
             option4Fld.setText(questn.getOption4());
             if(questn.getRightAnswer().equals(questn.getOption1())){
                 rightOptionComboBox.setSelectedIndex(0);
             }
             else if(questn.getRightAnswer().equals(questn.getOption2())){
               rightOptionComboBox.setSelectedIndex(1);
             }
             else if(questn.getRightAnswer().equals(questn.getOption3())){
                  rightOptionComboBox.setSelectedIndex(2);
             }
              else if(questn.getRightAnswer().equals(questn.getOption4())){
                  rightOptionComboBox.setSelectedIndex(3);
             }
           
     }
